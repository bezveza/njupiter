using System.Globalization;

namespace nJupiter.Configuration {

	internal class StringParser {

		/// <summary>
		/// Returns the default instance of IConfigHandler
		/// </summary>
		public static StringParser Instance { get { return NestedSingleton.instance; } }

		// thread safe Singleton implementation with fully lazy instantiation and with full performance
		private sealed class NestedSingleton {
			// ReSharper disable EmptyConstructor
			static NestedSingleton() {} // Explicit static constructor to tell C# compiler not to mark type as beforefieldinit
			// ReSharper restore EmptyConstructor
			internal static readonly StringParser instance = new StringParser();
		}

		public T Parse<T>(string value, CultureInfo culture) {
			var converter = TypeDescriptor.GetConverter(typeof(T));
			if(converter != null && converter.CanConvertFrom(typeof(string))) {
				return (T)converter.ConvertFromString(null, culture, value);
			}
			throw new InvalidConfigTypeException(string.Format("Can not find converter for type [{0}]", typeof(T).FullName));
		}

	}
}
